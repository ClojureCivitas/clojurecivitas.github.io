;; ## Macroexpand-Noj
;; 
;; Macroexpand-Noj centers around the growth and application of the Noj toolkit in real-world data science. It will feature talks that showcase practical projects, highlight new tools and libraries in the ecosystem, and share efforts around documentation, theoretical insights, and methodology. Whether you're a newcomer or an experienced Clojurian, our goal is that you find sessions that help you learn, explore, and contribute.
;; 
;; We will include follow-ups from Scinoj Light, practical use cases of Noj in data workflows, and discussions of newly emerging features that strengthen the Clojure data science experience.
;; 
;; ## Macroexpand-Deep
;; 
;; Macroexpand-Deep is the first Scicloj conference dedicated to AI system research and applications in Clojure. By "AI systems", we generally mean systems that combine deep learning networks with other components, though broader interpretations are welcome and encouraged.
;; 
;; The conference will explore how to build systems using large language models and vector embeddings, experiment with algorithms in specific domains, and train custom neural network architectures, and study the behavior of trained networks. We will also cover the creation and improvement of supporting tools and infrastructure that enable this kind of work in Clojure.
