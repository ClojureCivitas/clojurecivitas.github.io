;; I thought it was a cold. This happens a few times a year, where I get a
;; tickle in my throat and I know it’s the inkling for a cold. I slow down,
;; I drink some good tea, and the tickle smiles on its way out.

;; I thought I needed a gentle afternoon nap. And I did. But I didn’t only
;; need a nap. And it wasn’t only a tickle this time.

;; I had planned to enjoy all three days of Clojure conj 2025, but instead
;; I hit a surprise halfway in.

;; I returned to my hotel room for this afternoon nap, I laid down, and I
;; felt it — chills, aches, dull sensations in little regions I’m not used
;; to hearing from. Oh dear — it feels like at least a flu.

;; One afternoon nap later and I found myself ready for a second afternoon
;; nap. These naps were getting long, and it was evening now.

;; I could feel the impulse to fight it, to imagine situations where I
;; haven’t already walked over to the convention center for the last time.
;; But I could also feel myself beginning to accept it.

;; Isn’t this what programming is about too? I don’t aim to be surprised
;; while programming, but I have come to appreciate the surprise as the
;; interesting part, the part where I learn something, the part I remember.

;; And it’s foolish to trudge on with the same working model from before the
;; surprise happened. I must later or sooner accept the surprise just as it is.

;; After a tender cry over a takeout dinner from a hotel bar, I called a
;; good friend and went to sleep.

;; Friday morning I woke up and emailed to ask if my in-person ticket could
;; become a virtual ticket — asking for help can be a beautiful practice.

;; I felt glad to watch some sessions from my hotel room desk, but it feels
;; obvious I did miss out on in-person conversation. The people I met in the
;; first day and a half were lovely.

;; On the day I left home for the conference, in the morning while bathing,
;; I wondered what I might ask Rich Hickey. I found a question: what muses
;; would he credit for inspiring the work we know now as Clojure? I suspect
;; one such muse would be pain, but I wonder what he would say.

;; Maybe Rich has already given a talk about this — I haven’t watched all his
;; talks yet. Nobody can watch everything in this age.

;; Though two tests failed for flu and for COVID, I hope Rich doesn’t catch
;; whatever I had — I stood within five feet of him as I shared a jovial chat
;; for a while with the third presenter of the conference.

;; I feel like I partially met Rich simply seeing him — his posture was
;; natural and calm, much like his speaking voice and his approach to
;; software design.

;; So I accepted the surprise I hit at the halfway mark — I find it beautiful
;; now, but it took a moment. I partially evaluated Clojure conj 2025. Nobody
;; knows the future, but I hope to find myself called back.

